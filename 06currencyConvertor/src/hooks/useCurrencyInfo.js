import {useEffect, useState} from "react"

// We are making a custom hook here.
// This hook is used to access the json data from the api.
// Hooks are just functions that take some data and return something.

function useCurrencyInfo(currency){
    const [data, setData] = useState({})
    useEffect(() => {
        fetch(`https://cdn.jsdelivr.net/npm/@fawazahmed0/currency-api@latest/v1/currencies/${currency}.json`)
        .then((res) => res.json())  // api sends data in string(json) format, so we convert it into json.
        .then((res) => setData(res[currency]))  // from json data selects the key currency and sets it to data.
        console.log(data);
    }, [currency])
    console.log(data);
    return data
}

export default useCurrencyInfo;

